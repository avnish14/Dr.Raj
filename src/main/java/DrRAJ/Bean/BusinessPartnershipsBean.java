package DrRAJ.Bean;

public class BusinessPartnershipsBean {

	private String businessPartnershipsId;
	private String domain;
	private String query;
	private String name;
	private String emailId;
	private String contactNumber;

	public String getBusinessPartnershipsId() {
		return businessPartnershipsId;
	}

	public void setBusinessPartnershipsId(String businessPartnershipsId) {
		this.businessPartnershipsId = businessPartnershipsId;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getQuery() {
		return query;
	}

	public void setQuery(String query) {
		this.query = query;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}

}
